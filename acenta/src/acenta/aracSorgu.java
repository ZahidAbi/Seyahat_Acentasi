    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
    try {
        Connection con = DriverManager.getConnection(adres, username, password);

        Statement stat = con.createStatement();

        ResultSet res = stat.executeQuery("select * from acenta.firma");
        
        while(res.next()){
            jComboBox1.addItem(res.getString("isim"));
        }
        res.close();
        res = stat.executeQuery("select * from acenta.arac");
        HashSet<String> set=new HashSet<>();
        while(res.next()){
            set.add(res.getString("kalkis_yer"));
            set.add(res.getString("varis_yer"));
        }
        
        String[] sehirler=(String[]) set.toArray();
        
        for (int i = 0; i <sehirler.length; i++) {
        jComboBox2.addItem(sehirler[i]);
        jComboBox3.addItem(sehirler[i]);
        }
        
    } catch (SQLException ex) {
        Logger.getLogger(aracSorgu.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_formWindowOpened

